package com.company;

public class ReturnWithGetter {
    private  int number;
    private  String word;

    public void setNumber(int number){
        this.number = number;
    }

    public void setWord(String word){
        this.word = word;
    }

    public int getNumber() {
        return number;
    }

    public String getWord() {
        return word;
    }
}
